    public class Item {

        private String texto;
        public double valor;
        private int quant;

        public Item(int quant, String texto, double valor) {
            this.texto = texto;
            this.valor = valor;
            this.quant = quant;
        }

        @Override
        public String toString() {
            return texto;
        }

        public double getValor() {
            return valor;
        }

        public void setValor(double valor) {
            this.valor = valor;
        }

    }
